package Encapsulation;                  


//Encapsulation says that we should use methods to set the values and get the values through the object.

//It can be achieved by making the variable as private ,and using getters and setters to access it in other classes.

public class Encapsulation {
	
	private int i;

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}
	
	
	
	

}
